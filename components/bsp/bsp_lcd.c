esp_err_t lcd_clear(esp_lcd_panel_handle_t *panel, uint16_t color)
{
    uint16_t *buffer = heap_caps_malloc(LCD_WIDTH * sizeof(uint16_t), MALLOC_CAP_INTERNAL | MALLOC_CAP_8BIT);
    if (NULL == buffer) {
        ESP_LOGE(TAG, "Memory for bitmap is not enough");
        return ESP_FAIL;
    } else {
        for (uint16_t i = 0; i < LCD_WIDTH; i++) {
            buffer[i] = color;
        }
        for (int y = 0; y < LCD_HEIGHT; y++) {
            esp_lcd_panel_draw_bitmap(panel, 0, y, LCD_WIDTH, y+1, buffer);
        }
        heap_caps_free(buffer);
    }
    return ESP_OK;
}

esp_err_t lcd_clear_fast(esp_lcd_panel_handle_t *panel, uint16_t color)
{
    uint16_t fact = 120;
    // uint64_t s = esp_timer_get_time();
    uint16_t *buffer = heap_caps_malloc(LCD_WIDTH * fact * sizeof(uint16_t), MALLOC_CAP_SPIRAM);  //MALLOC_CAP_INTERNAL | MALLOC_CAP_8BIT
    if (NULL == buffer) {
        ESP_LOGE(TAG, "Memory for bitmap is not enough");
        return ESP_FAIL;
    } else {
        for (uint16_t i = 0; i < LCD_WIDTH * fact; i++) {
            buffer[i] = color;
        }
        for (int y = 0; y < LCD_HEIGHT; y += fact) {
            esp_lcd_panel_draw_bitmap(panel, 0, y, LCD_WIDTH, y + fact, buffer);
        }
        heap_caps_free(buffer);
    }
    // uint64_t t1 = (esp_timer_get_time() - s);
    // float time_per_frame = t1 / 1000;   
    // float fps = 1000000.f / (float)t1;
    // ESP_LOGI(TAG, "@resolution %ux%u  [time per frame=%.2fMS, fps=%.2f]", LCD_WIDTH, LCD_HEIGHT, time_per_frame, fps);
    return ESP_OK;
}

uint16_t rgb888_to_565(uint8_t r, uint8_t g, uint8_t b)
{
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}

